-- Variaveis para controle
quads = {}
tam = 32

function love.load()
    -- Carga da imagem do Sprite
    img = LG.newImage('mundo.png')

    -- Coletar os quads a serem usados
    quads[1] = Q(2 * tam, 0 * tam, tam, tam, tam, img:getWidth(), img:getHeight())
    quads[2] = Q(4 * tam, 0 * tam, tam, tam, tam, img:getWidth(), img:getHeight())
    quads[3] = Q(0 * tam, 20 * tam, tam, tam, tam, img:getWidth(), img:getHeight())
    quads[4] = Q(3 * tam, 5 * tam, tam, tam, tam, img:getWidth(), img:getHeight())
end

function love.draw()
    for p, row in ipairs(map_sprite) do
        for c, quad in ipairs(row) do
            if quad ~= 0 then
                LG.draw(img, quads[quad], (c-1) * tam, (p-1) * tam)
            end
        end
    end
end